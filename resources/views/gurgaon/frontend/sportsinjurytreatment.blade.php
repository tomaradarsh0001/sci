                        <h2 class="theme-color">Causes of sports Injury </h2>

                        
                        <p><strong>A sports injury can happen to anyone and majorly can be due to the following reasons- </strong></p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Accidents such as falls or wrong position while doing any physical activity </li>
