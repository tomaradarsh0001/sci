                        <p>The signs and symptoms of the common sports injury include- </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Pain, body ache, or tenderness </li>
                            <li><i class="fa fa-check"></i> Bruising </li>
                            <li><i class="fa fa-check"></i> Decrease range of motion </li>
                            <li><i class="fa fa-check"></i> Swelling </li>
