                            <li><i class="fa fa-check"></i> Exercising without warming up or cooling down the body </li>
                            <li><i class="fa fa-check"></i> Sudden changes in an exercise plan with which the body isn't familiar</li>
                            <li><i class="fa fa-check"></i> Participation in contact sports which involves extreme collision or tackling </li>
                            <li><i class="fa fa-check"></i> Taking part in activities involving jumping, running, or directional change frequently  </li>
