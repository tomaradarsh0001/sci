                        <p>This condition gets better on its own after some days. However, you can do various things to prevent and treat piles.   </p>
                        <p>Many adults suffer from piles from time to time. It occurs due to various reasons but its reason is not known. Fortunately, there are many effective options to treat hemorrhoids. Most people get relief with simple home remedies and lifestyle changes.  </p>
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Signs and Symptoms of Hemorrhoids </h2>

                        <p>Common Symptoms of hemorrhoids are:   </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> bleeding on passing stools   </li>
                            <li><i class="fa fa-check"></i> extreme itching in and around the anus   </li>
                            <li><i class="fa fa-check"></i> irritation and pain around the anus   </li>
                            <li><i class="fa fa-check"></i> fecal leakage  </li>
                            <li><i class="fa fa-check"></i> lumps formation around the anus</li>
                            <li><i class="fa fa-check"></i> painful or itchy lumps or swelling around the anus   </li>
                            <li><i class="fa fa-check"></i> feeling as if you need to pass stools after going to the toilet   </li>
                            <li><i class="fa fa-check"></i> slimy mucus in your underwear or on toilet paper after wiping your bottom   </li>
                            <li><i class="fa fa-check"></i> painful bowel movements   </li>
                        </ul>
                        <p>While hemorrhoids are painful, these are not life-threatening and also go on their own without treatment. If you’ve piles, you also develop signs of anemia like pale skin and weakness because of blood loss. Some hemorrhoid symptoms are also similar to that of other digestive tract issues. However, rectum bleeding also occurs due to Crohn’s disease, cancer of the rectum, etc.   </p>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Causes of Hemorrhoids</h2>

                        <p>Hemorrhoids, or piles, can occur due to genetic factors when passed on to a child from their parent. So, if your parents suffered from hemorrhoids, you are also likely to be affected by them in the future or might already have.   </p>
                        <p>Consistently being obese, heavy lifting, or having other persistent on your body could also increase the risk of getting piles.   </p>
                        <p>If you stand too much without taking any breaks sitting can be another reason for hemorrhoids. Consistent anal sex and diarrhea are other common reasons to increase the risk of developing hemorrhoids.   </p>
                        <p>You may develop piles even when you’re pregnant. When your uterus enlarges, it puts pressure on a vein in the colon and causes a bulge.   </p>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">Reason for Increased pressure in the vessels</h3>
                        <p>While the presence of piles is considered normal anatomy, many people and healthcare professionals refer to it as an abnormal condition due to swelling and other problems. </p>
                        <p>Pile swelling occurs when the pressure increases in small blood vessels. This further causes these vessels to increase in size and lead to various symptoms.  </p>
                        <p>The increased pressure in the vessels is caused due to various factors, such as:   </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Smaller caliber stool and low fiber diet cause an individual to strain while having a bowel movement and increase pressure in the blood vessels.   </li>
                            <li><i class="fa fa-check"></i> Prolonged sitting on a toilet seat can also increase pressure inside the hemorrhoid blood vessels. </li>
                            <li><i class="fa fa-check"></i> Pregnancy is also related to hemorrhoid swelling and is also likely to increase the pressure of the enlarged uterus on the anus and rectum. Additionally, the hormonal changes due to pregnancy weaken muscles supporting the anus and rectum.   </li>
                            <li><i class="fa fa-check"></i> Acute and chronic diarrhea  </li>
                            <li><i class="fa fa-check"></i> Colon cancer   </li>
                            <li><i class="fa fa-check"></i> Obesity</li>
                        </ul>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">Piles Treatment Options</h3>
                        <p><b>Pain relief: </b>To relieve pain, sit in a bathtub full of warm water for 10 minutes every day. You may even try sitting on a warm water bottle for relieving the pain of piles. If the pain is unbearable, try an over-the-counter suppository, cream, or ointment to relieve itching and burning.   </p>
                        <p><b>Fiber supplements:</b> To avoid constipation, take over-the-counter fiber supplements for softening stools.   </p>
                        <p><b>Topical creams:</b> You may even try some over-the-counter topical creams like hemorrhoid or hydrocortisone cream.   </p>
                        <p><b>Medical procedures: </b>If home remedies don’t work, your doctor can recommend using a rubber band ligation to cut off the circulation of piles by placing the rubber band around it. Due to a lack of circulation, the hemorrhoid is forced to shrink. But only get this procedure done by a medical professional – NOT at home. If this treatment doesn’t work, the doctor can perform injection therapy, known as sclerotherapy, to reduce the hemorrhoid's size.   </p>
                        
                        
