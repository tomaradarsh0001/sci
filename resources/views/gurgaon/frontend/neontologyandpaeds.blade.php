                        <p>General pediatricians offer routine care and treatment to children for various diseases. On the other hand, neonatologists concentrate on the health of premature or underweight newborns to improve their chances of growth and survival.  </p>
                        <p>You need both pediatricians and neonatologists to take better care of your newborn and the growing child. These specialists see your baby many times from birth till they turn 5. After 5 years, the pediatrician is likely to see your child for routine check-ups annually or monthly.  </p>
                        
                        
