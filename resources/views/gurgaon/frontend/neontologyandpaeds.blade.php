                        <h3 class="theme-color">What Types of Treatments Do Neonatologists Provide?</h3>
                        
                        <p>Neonatologists usually offer the following care:  </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Coordinate care and manage newborns that are critically ill, born premature, or need surgery.  </li>
                            <li><i class="fa fa-check"></i> Diagnose and treat infants with conditions like infections, breathing, and birth defects.  </li>
                            <li><i class="fa fa-check"></i> Ensure that some critically ill infants receive proper nutrition for growing and healing.  </li>
                            <li><i class="fa fa-check"></i> Provide care to infants at a cesarean or other delivery methods that may need medical intervention during delivery.  </li>
                            <li><i class="fa fa-check"></i> Consult with pediatricians, obstetricians, and family doctors about health conditions affecting infants.  </li>
                            <li><i class="fa fa-check"></i> Stabilize and treat infants with any life-threatening medical conditions.  </li>
                        </ul>
                        <p>Neonatologists work in newborn intensive care units or special care nurseries in hospitals. In many cases, after an infant has been discharged from the hospital, a neonatologist may offer short-term and follow-up outpatient care. The neonatologist also coordinates with a pediatrician to deal with certain health conditions.  </p>
                    
