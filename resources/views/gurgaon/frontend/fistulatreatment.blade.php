                        <h2 class="theme-color">What is Anal Fistula?</h2>
                        <p>An anal fistula is a medical term for a small tunnel that forms between the skin around the anus and the muscular opening at the end of the bowel or digestive tract. It occurs as a result of the infection around the anus, causing an abscess (pus) in nearby tissues. </p>
                        <p>Most anal fistulas develop abscess that further drains spontaneously or through a surgical technique from the skin near the anus. When the pus is drained, it leaves a small channel behind it.</p>
                        <p>Anal fistulas can lead to some unpleasant symptoms, such as skin irritation and discomfort. These are usually treated with surgery.</p>
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Signs and Symptoms of Anal Fistula</h2>

                        <p>Following are some common symptoms of anal fistula:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Skin irritation around or near the anus.</li>
                            <li><i class="fa fa-check"></i> Persistent and throbbing pain, which worsens as you sit down, pass stool, cough, or move around.</li>
                            <li><i class="fa fa-check"></i> Smelly discharge from the anus.</li>
                            <li><i class="fa fa-check"></i> Passing pus while passing stool.</li>
                            <li><i class="fa fa-check"></i> Inflammation and redness around the anus, as well as fever (high temperature) when you have got an abscess.</li>
                            <li><i class="fa fa-check"></i> Difficulty to control bowel movements in some cases.</li>
                        </ul>
                        <p>You might even notice symptoms such as:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Bleeding while passing stool.</li>
                            <li><i class="fa fa-check"></i> Painful urination or bowel movements.</li>
                            <li><i class="fa fa-check"></i> A foul-smelling fluid discharge oozed from the anal hole.</li>
                        </ul>
                        <p>The end of the fistula may be visible in the form of a hole near the skin around the anus, but it is difficult to see it yourself.</p>
                        <p>If you notice or experience any of these signs or symptoms lately, you must seek medical help immediately. Do consult an experienced specialist to get the best treatment to cure this condition.</p>

                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Types of Anal Fistulas</h2>

                        <p>The different types of anal fistulas are categorized as per their location, such as:</p>

                        <p class="">
                            <i class="fa fa-check" style="color: #c84c55;"></i><b> Intersphincteric fistula:</b>  In this type, the tract starts within the space between the external and internal sphincter muscles, as well as open near the anal opening.
                        </p>
                        <p>
                            <i class="fa fa-check" style="color: #c84c55;"></i> <b>Transphincteric fistula: </b>The tract starts within the space between external and internal sphincter muscles or behind the anus. It further crosses through the external sphincter and further opens outside the anal opening.
                        </p>
                        <p>
                            <i class="fa fa-check" style="color: #c84c55;"></i> <b>Fistula: </b>In this type, the tract starts in a space between internal and external sphincter muscles. It then turns upward to a position just above the puborectalis muscle to cross this muscle. It also extends downward between the levator ani and puborectalis muscle and then opens outside the anus.
                        </p>
                        <p>
                            <i class="fa fa-check" style="color: #c84c55;"></i> <b>Extrasphincteric fistula:</b>  In this type, the tract starts at the sigmoid colon or rectum and then extends downward. It passes via the levator ani muscle to open a few inches near the anus.
                        </p>

                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">Best Treatment Options for Fistula</h3>
                        <p>The treatment also depends on the location of the fistula. Here are some common treatment options:</p>
                       
                            <p>
                                <i class="fa fa-check"></i> <b>Fistulotomy:</b> In this treatment the surgeon probes to look for the internal opening of the fistula. After that, the tract is cut open and all the contents are scraped and flushed out for opening the fistula.

                            </p>
                            <p><i class="fa fa-check"></i> <b>Advancement Rectal Flap: </b> The surgeon will core out the tract to cut a flap in the rectal wall for removing the fistula’s internal opening for drainage. for creating scar tissue near the sphincter muscle before cutting it through a knife. It also aids drainage of the fistula, besides opening and closing the fistula tract.</p>
                            <p><i class="fa fa-check"></i> <b>Collagen plug or Fibrin Glue: </b>In many cases, the fibrin glue (made from plasma protein) can be used for sealing and healing the fistula. The glue is injected via an external opening to clear the tract. A collagen plug can also be used for sealing and closing the fistula tract.</p>
                            

