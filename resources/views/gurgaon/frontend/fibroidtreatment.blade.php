                    <div class="pt-2 pt-md-4">
                        <p>Uterine fibroids are benign (noncancerous) growths in the womb or uterus of women. These are also known as myomas or leiomyomas.   </p>
                        <p>The uterine fibroids develop from the size of a seedling to a bulky mass. A female may have a single fibroid or several fibroids. In some cases, the multiple fibroids may expand their uterus to an extent that it reaches their rib cage.   </p>
                        <p>Most women get uterine fibroids at any time in their lives. However, you may not know about the condition, as it often shows no symptoms. The doctor can discover it incidentally during a prenatal ultrasound or pelvic examination.   </p>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Who Can Develop Fibroids?  </h2>

                        
                        <p><strong>While it’s not clear why women develop fibroids; however, some patterns that have been observed over the years are:   </strong></p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Age Group: These usually develop in women aged between 30 and 40.   </li>
                            <li><i class="fa fa-check"></i> Race: Often black women are at a higher risk of developing fibroids compared to women of some other racial groups. Besides, black women even develop fibroids at younger ages and are also likely to develop larger fibroids with some severe symptoms.   </li>
                            <li><i class="fa fa-check"></i> Heredity: If your relatives like your mother or sister had fibroids, you are at a higher risk of developing them.   </li>
                            <li><i class="fa fa-check"></i> Other factors: Other factors that may increase the risk of developing fibroids are vitamin D deficiency, menstruation onset at an early age, drinking alcohol, obesity, having a high red meat diet, etc.   </li>
                        </ul>
                        
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Signs and Symptoms of Fibroids</h2>
                        <p>The symptoms of fibroids usually depend on the types and number of tumors one has. For instance, submucosal fibroids cause trouble conceiving and result in heavy menstrual bleeding. </p>
                        <p>If the tumor is very small or the sufferer is going through menopause, they may not notice any symptoms. The fibroids may shrink at the time of menopause or after it. It happens because women in the menopause phase experience reduced levels of progesterone and estrogen – hormones that stimulate the growth of fibroids.  </p>
                        
                        <h2 class="theme-color">Common symptoms of fibroids are</h2>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Pain in the lower back or pelvis   </li>
                            <li><i class="fa fa-check"></i> Heaving bleeding during or between the periods</li>
                            <li><i class="fa fa-check"></i> Increases urination   </li>
                            <li><i class="fa fa-check"></i> Pain during intercourse   </li>
                            <li><i class="fa fa-check"></i> The menstruation cycle lasts longer than usual   </li>
                            <li><i class="fa fa-check"></i> Enlargement or swelling in the abdomen  </li>
                            <li><i class="fa fa-check"></i> Pressure in the lower abdomen </li>
                        </ul>
                        <p>Uterine fibroids are usually categorized as per their location. The intramural fibroids develop inside the muscular uterine wall. Subserosal fibroids develop just outside the uterus, while submucosal fibroids bulge within the uterine cavity.   </p>

                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">What Complications can Occur with Uterine Fibroids?  </h3>
                        <p>Generally, uterine fibroids are not dangerous to health. However, they may lead to certain complications and can also cause some discomfort. For instance, the sufferer may experience anemia (reduced red blood cells), which further causes fatigue due to heavy blood loss. In very rare cases, a blood transfusion is required to recover the blood loss.   </p>
                        <div class="mt-3 mt-lg-6"></div>

                        <h3 class="theme-color">Pregnancy complications due to fibroids</h3>
                        <p>Usually, fibroids won’t interfere with pregnancy or getting pregnant. However, a specific type of fibroid – submucosal fibroid – could lead to pregnancy loss or female infertility.   </p>
                        <p>The fibroids may even increase the risk of some pregnancy complications like fetal growth restriction, placental abruption, and preterm delivery. </p>
                        
                        <h3 class="theme-color">Fibroids Diagnosis</h3>
                        <p>If you experience the common symptoms of uterine fibroids, a doctor may perform the following tests for diagnosis: </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Lab tests: For abnormal menstrual bleeding, the doctor may ask for certain lab tests for investigating the potential causes. These tests include a complete blood count (CBC) for determining whether you have anemia due to chronic blood loss. The blood tests also rule out thyroid problems or bleeding disorders.   </li>
                            <li><i class="fa fa-check"></i> Magnetic resonance imaging (MRI): This imaging test is done to show the detailed size and location of the fibroids. The MRI is usually done on women approaching menopause or women with a larger uterus.   </li>
                        </ul>
                        
                        <div class="mt-3 mt-lg-6"></div>

                        <h3 class="theme-color">Medications </h3>
                        <p>Medications are used for regulating hormone levels and shrinking fibroids. </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Gonadotropin-releasing hormone (GnRH) agonists like Lupron are prescribed for shrinking fibroids and dropping estrogen and progesterone levels.   </li>
                            <li><i class="fa fa-check"></i> GnRH antagonists, such as Cetrotide and ganirelix acetate, also shrink fibroids by stopping the production of luteinizing hormone (LH) and follicle-stimulating hormone (FSH).   </li>
                        </ul>
                        <p><strong>Other options to control bleeding and pain are:   </strong></p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> over-the-counter (OTC) pain relievers like Advil   </li>
                            <li><i class="fa fa-check"></i> an intrauterine device (IUD) for releasing the hormone progestin   </li>
                            <li><i class="fa fa-check"></i> birth control pills   </li>
                        </ul>
                        <div class="mt-3 mt-lg-6"></div>

                        <h3 class="theme-color">Minimally Invasive procedures </h3>
                        <p>The advanced non-invasive surgical technique is forced ultrasound surgery (FUS). In this method, high-frequency, high-energy sound waves are directed to destroy the fibroids.   </p>
                        <p>Even endometrial ablation can be done to insert a special tool into the uterus for destroying fibroids, using electric current, heat, or hot water.   </p>
                        <p>Another surgical option is uterine artery embolization. In this treatment, the blood supply to fibroids is cut off.   </p>
                        
                        <div class="mt-3 mt-lg-6"></div>

                        <h4 class="theme-color">Uterine Fibroid Surgery and Treatment Options  </h4>
                        <p>The fibroids can also be removed from the uterus by traditional surgical procedures, such as:   </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i>  Abdominal myomectomy: This surgical treatment is performed if a patient has multiple fibroids, deep fibroids, or very large fibroids. For this procedure, the doctor performs open abdominal surgery for removing all the fibroids. For most women who are suggested to undergo a hysterectomy for removing fibroids, an abdominal myomectomy is a good alternative. However, the scarring caused by this surgery may affect future fertility.   </li>
                            <li><i class="fa fa-check"></i>  Hysterectomy: This is another surgical option, which is used to remove the entire uterus. It is the only permanent solution for removing recurring uterine fibroids. Hysterectomy may end the ability to bear children in the future. This procedure can also elect ovaries to be removed. After this surgery, the women get menopause and may even require hormone replacement therapy. However, some women having uterine fibroids may get to keep their ovaries.   </li>
                        </ul>
                       
