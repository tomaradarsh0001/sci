                        <h2 class="theme-color">What is pediatric Orthopedic?</h2>

                        <p>Pediatric orthopedics is a medical specialty focused on treating deformities of bone, joint, back or muscle in a child. The treatableage includesinfantsthrough teenagers.  The deformities can be congenital (present from birth) or due to accidents, strenuous physical activity orgrowth anomaly.General bone issues in children do not require pediatric orthopedic care, as any general physician will treat them. But for specific issues, pediatric orthopedics is sought.</p>
                        <p>The pediatric orthopedics is more challenging and evolved than general orthopedics. Pediatric orthopedics has to deal with rapidly growing and highly elastic bones. That’s exactly the other way around in general orthopedics. Certain musculoskeletal issues are found in children but not in adults. Plus, every child may not be able to express his/her issues like an adult can.</p>
                        
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">What Types of Problems Do Pediatric Orthopedic Surgeons Treat?</h3>
                        <p>Kids are vulnerable to several musculoskeletal issues. The minor conditions are treatable by general physicians while the complex ones require pediatric orthopedic intervention.Ensure prompt pediatric orthopedic care to your child if he/she has:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Conditions specific to spine, such as Scoliosis, Kyphosis, and Lordosis</li>
                            <li><i class="fa fa-check"></i> Cerebral palsy, muscular dystrophy, and other neuromuscular issues</li>
                            <li><i class="fa fa-check"></i> Crooked limbs, angular deformities, and legs of varying sizes</li>
                            <li><i class="fa fa-check"></i> Hip issues, such as Perthes Disease, SCFEand hip dysplasia</li>
                            <li><i class="fa fa-check"></i> Knee deformities, including ACL, OCD, Meniscus tear, Patellar volatility</li>
                            <li><i class="fa fa-check"></i> Infections and tumors in joints and bones</li>
                            <li><i class="fa fa-check"></i> Fractured bones, bone cysts and sports injuries</li>
                            <li><i class="fa fa-check"></i> Shoulder problems, such as SLAP tears</li>
                            <li><i class="fa fa-check"></i> Developmental delays and Down’s syndrome</li>
                        </ul>
                        <p>The pediatric orthopedic surgeon is trained to diagnose, treat, and manageallthese conditions. Certain deformities, notably developmental delays may require a multi-modality approach.</p>
                       
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">Pediatric Orthopedic Surgeons: Specialty Care for Children</h3>
                        <p>Children are special and so are their musculoskeletal treatment requirements.The child may be yet to develop language skills or feel too scared to tell about his/her problem.The pediatric orthopedic surgeon is supposed to determine the child’s condition and provide feasible surgical and non-surgical intervention strategies accordingly. He/she is well trained to deal with the issues posed by the child’s rapidly developing bones. If need be, the surgeon creates a patient-focused rehabilitation program involving the active participation of the child’s family. The idea is to ensure medical, physical and emotional support throughout the recovery process. The surgeon may consider providing multi-disciplinary care for specific musculoskeletal deformities.</p>
                        
