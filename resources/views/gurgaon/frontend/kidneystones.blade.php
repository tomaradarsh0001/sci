                        <h2 class="theme-color">Causes Of Kidney Stones</h2>
                        <p>Kidney stones develop when certain substances present in the urine turn into crystals. These substances are often called calcium stones; however, uric acid and other minerals can also lead to stones. </p>
                        <p>Around 80% of people suffer from calcium kidney stones, 5-10% of people suffer from uric acid stones and 10% of stones are made from the mineral struvite. Typically, these substances dissolve in the urine. However, some medical conditions also lead to an increase in the concentration of these substances, resulting in the formation of crystals. Even some foods like rhubarb include higher levels of oxalic acid that increase uric acid concentration in urine. </p>

