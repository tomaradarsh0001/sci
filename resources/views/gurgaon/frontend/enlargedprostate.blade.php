                       <h2 class="theme-color">Risk Factors Of BPH</h2>
                       <p><strong>Ageing:</strong> Prostate gland enlargement rarely presents with specific signs and symptoms in men under the age of 40.  Around one-third of men notice moderate to severe symptoms around the age of 50 and some do so around the age of 80 when it comes to enlarged prostate problems. </p>
                        <p><strong>Family history:</strong> If any blood relative, such as a brother or father, has enlarged prostate problems, you’re also likely to have them.</p>
                        <p><strong>Heart disease and diabetes:</strong> According to the studies, heart disease and diabetes can also enhance the risk of BPH.</p>
                        <p><strong>Obesity: </strong>Being overweight can also increase the risk of prostate enlargement and problems associated with an enlarged prostate, for which one may require treatment. Regular exercise, on the other hand, can help lower this risk.</p>
                        
