                        <h3 class="theme-color"><b>Treatment Options for BPH</b></h3>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> BPH Natural Treatment: Some natural enlarged prostate treatment methods for treating BPH are:</li>
                            <li><i class="fa fa-check"></i> Urinating whenever you feel the urgency.</li>
                            <li><i class="fa fa-check"></i> Avoiding over-the-counter antihistamine or decongestant medications that may make the bladder hard to empty.</li>
                            <li><i class="fa fa-check"></i> Avoiding caffeine and alcohol, especially after dinner.</li>
                            <li><i class="fa fa-check"></i> Exercise regularly to lower the symptoms.</li>
                            <li><i class="fa fa-check"></i> Staying warm is important, as cold temperatures could potentially worsen the symptoms.</li>
                            <li><i class="fa fa-check"></i> BPH Drugs: When lifestyle changes don’t relieve the symptoms, the doctor would prescribe certain medications for enlarged prostate treatment.</li>
                            <li><i class="fa fa-check"></i> Hormone reduction medications: These medications are used for reducing the hormonal levels released by the prostate gland. Finasteride and dutasteride are usually prescribed to decrease the levels of testosterone. This is done because sometimes lowering hormone levels can also make the prostate smaller, enabling convenient urine flow to facilitate enlarged prostate treatment.</li>
                            <li><i class="fa fa-check"></i> Antibiotics: Even antibiotics can be used for the treatment of the prostate that has become severely inflamed due to bacterial prostatitis. Taking antibiotics for treating bacterial prostatitis can improve BPH symptoms by reducing inflammation. However, antibiotics cannot relieve the inflammation or prostatitis caused due to bacterial infection.</li>
                            <li><i class="fa fa-check"></i> Alpha-1 blockers: These medications are used to relax prostate and bladder muscles. These medications relax the neck of the bladder for convenient urine flow. Some examples of alpha-1 blockers prescribed for enlarged prostate treatment are:</li>
                            <li><i class="fa fa-check"></i> Prazosin</li>
                            <li><i class="fa fa-check"></i> Doxazosin</li>
                            <li><i class="fa fa-check"></i> Alfuzosin</li>
                            <li><i class="fa fa-check"></i> Tamsulosin</li>
                            <li><i class="fa fa-check"></i> Terazosin</li>
                           
                        </ul>
                        
                        
