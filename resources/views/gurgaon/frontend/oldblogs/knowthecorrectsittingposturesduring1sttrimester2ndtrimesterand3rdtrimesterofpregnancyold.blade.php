                        <h3 class="blog_title">Know the Correct Sitting Postures during 1st Trimester, 2nd Trimester and 3rd Trimester of Pregnancy</h3>
                        <p>Congrats! You are going to be a mother soon and you must be pretty excited about this fact. You will be looking forward to a healthy pregnancy; however, at times pregnant women complain about back pain and neck pain. The back pain experienced throughout pregnancy is due to the strain on the back, as a result of the baby’s growing weight. Maintaining a good posture is the only cure to avoid back pain.</p>
                        <h5>What Does Good Posture Involve? </h5>
                        <p>You come across the term ‘good posture’ or ‘proper posture’ during pregnancy. Do you have any idea? If you don’t have any idea about it, don’t freak out. This post will guide you with the details.</p>
                        <p>It’s nothing but training the body to sit and even stand or walk in such a way that the strain on the back can be reduced to a minimum. Whether one is sitting or standing, the body should be aligned properly. Good posture is considered to be an important contributor to a healthy pregnancy. A wrong sitting posture not only causes discomfort but might also harm the baby. Studies have shown that poor posture leads to complications and joint problems even after delivery.</p>
                        
                        
                        <h5>Tips for Maintaining the Right Sitting Posture </h5>
                        <p>If you are a working professional, maintaining proper posture while sitting is necessary. Let’s take a look at the tips for maintaining the correct posture.</p>
                        <ul class="list_blog">
                            <li>Make sure you don’t sit for long hours at a stretch during the 1st trimester of pregnancy. Take short breaks after every one hour and whenever you are sitting, keep your back and shoulder straight. </li>
                            
                        </ul>
                        <p>As the baby starts to grow from the 2nd trimester onwards, you will need to change the way you used to sit.</p>
                        <ul class="list_blog">
                            <li>While sitting, don’t forget to keep your back straight and the shoulders must be pulled back. The buttocks should reach the back of the chair.</li>
                            <li>Choose a chair that comes with good back support. Place a pregnancy pillow behind the lower-back portion.</li>
                            <li>Use a footstool and keep your feet raised. It would reduce the strain on the lower back. If you don’t have a footstool, your feet should be placed evenly on the floor. The knees and the hips must be at 90 degrees. </li>
                        </ul>
                       
                        <p>In the event, you are not using a chair with lumbar support; you should keep in mind the following things. They are:</p>
                        <ul class="list_blog">
                            <li>Don’t cross your legs while sitting. It can affect blood circulation.</li>
                            <li>You should slightly tilt your pelvic forward so that sway back position is avoided.</li>
                            <li>Don’t slouch while sitting on the chair.</li>
                            <li>If you are sitting on a chair that rolls, don’t just turn your waist. Instead, you should turn your whole body.</li>
                            <li>Adjusting the chair height with the workstation is vital to keep the shoulder relaxed. Make it a point to sit up close to your desk. Allow the elbows and arms to rest on the chair/ desk.</li>
                        </ul>
                        
                        
                        
                        <p>These are some rules for maintaining the correct sitting posture during pregnancy. What are you waiting for? Follow these rules and get rid of back pain and strain during your pregnancy.</p>
