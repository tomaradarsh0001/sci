                        <h3 class="blog_title">How to get rid of Hemorrhoids (Piles)? – Symptoms & Treatment</h3>
                        <p>Hemorrhoids,commonly known as Piles, are swollen veins in the anus and the lowest part of the rectum. This causes painful discomforts and rectal bleeding. Hemorrhoids have become very common in adults.</p>
                        <p>The swelling and inflammation of the veins can develop inside rectum, known as internal hemorrhoids; or around the rectum, known as external hemorrhoids. External Hemorrhoids are generally more common.</p>
                        <p>Hemorrhoids can be passed down the family. If anyone in your family has had a medical history of it, you are more likely to have it too. Building up pressure in the lower rectum can make the veins present there swell and bulge. This may happen due to the following reasons-</p>
                        <ul class="list_blog">
                            <li>Obesity</li>
                            <li>Pregnancy</li>
                            <li>Low fibre diet</li>
                            <li>Chronic Diarrhea or Constipation</li>
                            <li>Straining during the bowel movements</li>
                            <li>Heavy lifting on a regular basis</li>
                            <li>Urinary Hesitency</li>
                        </ul>
                        
                       
                        <p>The risk factors of having hemorrhoids will increase with age as the veins in the rectum area gradually weaken and stretch. During pregnancy, the baby growing inside the uterus can put pressure on the rectum tissues and thus result in piles.</p>
                        <h5>Symptoms of Piles </h5>
                        <p>The symptoms of hemorrhoids or piles depend on the type, ie., Internal Hemorrhoids & External Hemorrhoids.</p>
                        
                        <h5>Symptoms of Internal Hemorrhoids are-</h5>
                        <p>Bleeding during bowel movements</p>
                        <p>Bulging of tissues through the anal opening which is the prolapsed or protruding hemorrhoids. This can be very painful.</p>
                    
                        <h5>Symptoms of External Hemorrhoids are-</h5>
                        <p>Pain during bowel movements</p>
                        <p>Bleeding during bowel movements</p>
                        <p>Swelling around the anus</p>
                        <p>When a blood clot from external hemorrhoids turns blue or purple, it results in thrombosis or thrombosed hemorrhoids. This can lead to symptoms like-</p>
                        <ul class="list_blog">
                            <li>Severe pain & discomfort</li>
                            <li>Bleeding</li>
                            <li>Itching & inflammation</li>
                            <li>Lump around the anus</li>
                            <li>Very painful.</li>
                        </ul>
                        
                        <h5>Treatment of Piles</h5>
                        <p>Smaller hemorrhoids can go away on their own. But if it remains untreated for a longer time, the complications may increase. If you are facing any of the symptoms, schedule an appointment immediately and get diagnosed.</p>
                        
                        <h5>How is the diagnosis of hemorrhoids done?</h5>
                        <p>Before any treatment is done, diagnosis of hemorrhoids has to be done to determine its severity. Firstly, the doctor would ask about you and your family’s medical history. They will also ask about all your symptoms.</p>
                        <p>Next , the doctor will conduct one or both of the following examinations-</p>
                        <p>Physical Exam: To examine the anus and recum region for any lumps, swelling or any other problems.</p>
                        <p>Digital Exam: The doctor will put on gloves and apply lubrication. Then, insert a small finger in the rectum to check for any lumps, unusual growths or swellings.</p>
                        <p>After this, one among these few tests may be performed to check for Internal Hemorrhoids and other risk factors.</p>
                        <p>Anoscopy: Anoscope, a short plastic tube, will be used to look into the anal canal.</p>
                        <p>Practoscopy- Used to look into the Rectum</p>
                        <p>Sigmoidoscope: A flexible lighted tube called sigmoidoscope, is used to look into the rectum and collect tissue samples for other tests.</p>
                        <p>Colonoscopy: The entire large intestine will be examined with a colonoscope and tissues will be collected for any other tests.</p>
                        
                        
                        <h5>Treatment</h5>
                        <p>Once the diagnosis is done, the treatment options will be weighed. Depending on the types of the hemorrhoids, the doctor will choose one of the following treatment procedures.</p>
                        
                         <h5>Medications</h5>
                        <p>If the hemorrhoids are not large and cause only painful discomfort, then the doctor will prescribe over-the-counter medications, ointments or creams. These will help to ease the pain and irritation. Use them as prescribed by the doctor.</p>
                        
                         <h5>Hemorrhoid Thrombectomy</h5>
                        <p>In case of formation of thrombosis or thrombosed hemorrhoid in an external hemorrhoid, it will be removed through this procedure and provide some relief. This is most effective when done within 72 hours of the formation of clot.</p>
                        
                        <h5>Surgery</h5>
                        <p>When the medicines don’t work, one might need surgery. Your doctor can go for some minimally invasive procedures like rubber band ligation, injecting chemicals to shrink hemorrhoids (sclerotherapy) or coagulation techniques by usage of laser.</p>
                        <p>If still the hemorrhoids is not treated, then the doctor might consider the following surgeries-</p>
                        <p>Hemorrhoidectomy: This procedure is done to remove the hemorrhoids under anesthesia. Excessive tissues that cause bleeding are removed through this surgery.</p>
                        <p>Hemorrhoid Stapling: Also known as Stapled Hemorrhoidopexy, it blocks the blood flow to hemorrhoidal tissue and is used only for internal hemorrhoids. It is less painful than hemorrhoidectomy.</p>
                        
                        <h5>Home Remedies</h5>
                        <p>Home remedies help in early cases or reduce recurrence after surgery. This will include some common lifestyle changes. You can follow the following remedies-</p>
                        <p>Having a high fiber diet. This includes eating more vegetables, fruits and grains to help soften the stool. Doing this will help ease discomfort.</p>
                        <p>Soak in warm water regularly for 10-15 mins several times a day. Or a sitz bath can also help in relieving the burning sensation.</p>
                        <p>Drink plenty of water. This will help in easier bowel movements.</p>
                        
                        <p>Use of ice packs can help to ease the pain.</p>
                        <p>Avoid sitting for long hours</p>
                        <p>Avoid weight lifting</p>
                        <p>Hemorrhoids are not dangerous. It can be easily prevented by choosing a healthy lifestyle. If you are facing any problems in bowel movements, then do not delay in the seeking help. Delay in seeking help can lead to an increase in complications of hemorrhoids.</p>
                        
