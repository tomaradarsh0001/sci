                        <h3 class="blog_title">What is Hysterectomy? Purpose And Procedure</h3>
                        
                        <p>Hysterectomy is a kind of operation that is done for removing the uterus. The uterus is the womb that helps develop the baby during a woman’s pregnancy. The uterine lining secretes menstrual blood. In the majority of cases, the whole uterus will be removed during a hysterectomy.</p>
                        <p>In addition, the surgeon may remove other reproductive organs such as the ovaries and fallopian tubes. So, the extent to which hysterectomy is done depends on the underlying reason.</p>
                        <h5>Why is a hysterectomy done?</h5>
                        <p>The purpose of carrying out the operation may be for various reasons. These include the following.</p>
                        <ul class="list_blog">
                            <li>Uterine fibroids – non-malignant tumours that grow in the uterus causing blood loss,
pain or several other problems</li>
                            <li>Uterine prolapse – a condition in which the uterus shifts from the normal position and
gets into the vaginal channel</li>
                            <li>Uterine cancer</li>
                            <li>Cervical cancer</li>
                            <li>Ovarian cancer</li>
                            <li>Endometriosis – a disorder that comes into being with the growth of the inner lining of the uterus outside its cavity leading to blood loss and pain</li>
                            <li>Unusual and uncontrollable vaginal bleeding</li>
                            <li>Persistent pelvic pain</li>
                            <li>Pelvic inflammatory disease – a condition marked by the reproductive organs getting severely infected</li>
                            <li>Adenomyosis or the thickening and enlargement of the uterus</li>
                        </ul>
                        <p>For noncancerous causes, gynaecologists suggest hysterectomy when every other treatment approach has proven futile.</p>
                        <h5>Hysterectomy – The procedure</h5>
                        <p>Hysterectomy can be conducted in many ways. Each of the methods requires either local or general anaesthesia.</p>
                        <p>All through the procedure, you will remain unconscious by the general anaesthesia. This will  make sure you do not experience pain. When local anaesthesia is used, you will feel numbness below your waistline. But, in the course of the surgery, you will be awake. Often, local anaesthesia is mixed with a sedative during the procedure to make you feel drowsy and slightly comfortable.</p>
                        <h5>1. Abdominal hysterectomy</h5>
                        <p>In the course of an abdominal hysterectomy, your uterus will be removed by the doctor by bmaking a large incision on your abdomen. The incision is made in either a horizontal or vertical way. Regardless of the direction of the incision, both approaches show good healing tendency and leave behind faint scarring that melts away with time.</p>
                        <h5>2. Vaginal hysterectomy</h5>
                        <p>Amid this procedure, your doctor will make a tiny cut inside your vagina and remove the uterus through it. Since the cut is not external, a vaginal hysterectomy does not leave behind any noticeable scars.</p>
                        <h5>3. Laparoscopic hysterectomy</h5>
                        <p>In this procedure, a laparoscope is used. It is a small instrument in the form of a thin, long tube fitted with a light source and camera at its front. The light source has high intensity. The camera, too, has an exact resolution. During a laparoscopic hysterectomy, three to four tiny incisions will be created on the abdomen. Your surgeon will insert the instrument through the incisions. The light and camera will help the surgeon to locate your uterus. Once the uterus is traced, your surgeon will have the organ shredded into pieces and take out the pieces one after another.</p>
                        <p>The time after hysterectomy</p>
                        <p>Once you undergo a hysterectomy, you will no longer have menstrual periods. Also, you will lose the ability to conceive a child again. If your ovaries were removed simultaneously, menopause would set in immediately. If there was no removal of your ovaries, you might go into menopause much before than otherwise.</p>
                        
                        
