                        <h3 class="blog_title">Kidney Stones: Early Warning Signs and Symptoms</h3>
                        <p>Are you suffering from kidney stones? If you said yes, it’s nothing new. Since ancient times, stone diseases, like kidney stones, have been used to afflict humanity. Previously, there were no treatments for it. Now, with advancements in the field of medical science, treatment for kidney stones is easily available.</p>
                        
                        <h5>Kidney Stones: A Serious Health Issue</h5>
                        <p>A wide number of people are suffering from kidney stones. Based on a survey carried out by the National Kidney Foundation, it was found that one out of ten individuals suffers from kidney stones.</p>
                        
                        
                        <h5>What Are Kidney Stones?</h5>
                        <p>A kidney stone is also known by the name of ‘Nephrolithiasis’ or ‘Renal Lithiasis’, is a form of hard mineral deposits in the kidneys. It’s a collection of various minerals and salts inside the kidney.</p>
                        <p>The stones are crystalline in form and its size varies. Some stones can be very small as a grain; whereas some stones could be big just like a pebble.</p>
                        <p>Kidney stones can affect the kidneys and bladder if it’s left untreated. The best way to avoid kidney stones is to make some lifestyle and dietary changes. For instance, drinking plenty of water or keeping the weight under check or limiting the Sodium intake, and so on.</p>
                        
                       
                        
                        <h5>Detecting Kidney Stones </h5>
                        <p>People are becoming health conscious. They want to know if they are having stones in their kidney or not. It’s possible to know if they are having kidney stones or not by taking a look into some early signs.</p>
                        <p>Without further ado, let’s take a look into these signs.</p>
                        <p><b>Pain: </b>Pain caused by a kidney stone, also known as renal colic is severe. The pain is similar to the pain during childbirth. The pain starts all of a sudden and can last for a long time. One can feel the pain on the sides and below the ribs. During urination, the pain might get intense and give a burning sensation. These are sure indicators of kidney stones.</p>
                        <p><b>Nausea: </b>People with a kidney stone will get a feeling of nausea. It gets triggered as the kidneys and the gastrointestinal tracts share the same nerve connection.</p>
                        <p><b>Blood in Urine: </b>When one sees brown or red blood in their urine, it’s a sign of kidney stones being present in the urinary tract.</p>
                        <p><b>UTI Symptoms:</b> A person with a kidney stone may notice similar symptoms related to Urinary Tract Infection, such as pain during urination, smelly and discolored urine. Anyone experiencing these signs should visit a doctor to check if they have kidney stones or not.</p>
                        <p><b>Urine Flow Blockage: </b>If a person is unable to urinate, it could be a symptom of kidney stones. Often, the stones slow down the urine flow. Seeing a doctor at this time is important.</p>
                        <p><b>Fever: </b>A fever along with other symptoms of kidney stones is a sure indicator that a person suffers from kidney stones.</p>
                        <p><b>Pee Often: </b>The need to urinate often is another sign of stones being present in the urinary tract.</p>
                        <p>With proper lifestyle and dietary changes, the chance of suffering from kidney stones can be greatly lowered. If you feel you are suffering from kidney stones, get in touch with a doctor today</p>
                        
                        
