                        <h2 class="theme-color">What is Orthopaedics? </h2>

                        <p>Orthopaedics is a field of medical science focusing on diagnosing, preventing, correcting, and providing treatment to patients suffering from skeletal deformities of joints, bones, ligaments, muscles, nerves, tendons, and skin. All these elements form the complete musculoskeletal system. </p>
                        <p>The musculoskeletal system is a complicated system of joints, bones, tendons, ligaments, nerves, and muscles to enable you to move and be active. Once concentrated only on the care of children with limb and spine deformities, orthopaedics now takes care of patients of all ages – from newborns to the elderly. </p>
                        <p>The medical specialists who have expertise in this field are known as orthopaedists or orthopaedic surgeons. </p>
                        
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Conditions and Treatments Offered </h2>
                        <p>The general orthopaedic conditions are: </p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Osteoarthritis: It is a degenerative joint disease (DJD) that causes progressive damage to particular cartilage and the formation of bone at a joint’s margin. </li>
                            <li><i class="fa fa-check"></i> Osteoporosis: It is a group of disorders that leads to the reduction of bone mass per unit of the patient’s natural bone volume. </li>
                            <li><i class="fa fa-check"></i> Fibromyalgia: It is a chronic muscle pain disorder with no known cure or cause. It is the stage of soft tissue inflammation that causes body and muscle pain, along with pelvic pain and overwhelming fatigue. </li>
                            <li><i class="fa fa-check"></i> TMJ Dysfunction: It is the inflammation in the jaw joint, leading to difficulty in opening the mouth completely. It also causes jaw, facial, head, and neck pain. Other symptoms include dizziness, headaches, nausea, and tinnitus. </li>
                        </ul>
