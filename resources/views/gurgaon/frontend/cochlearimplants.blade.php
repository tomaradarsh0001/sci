                        <h2 class="theme-color">Cochlear Implant Consists</h2>

                        <p>A <a href="https://scihospital.com/blog/facts-one-must-know-about-cochlear-implants"><b><u style="color:black">cochlear implant</u></b></a> is an electronic device, which is surgically implanted in the inner ear
to directly stimulate the hearing nerve. The cochlear implant consists of:</p>
                        
                        <h3 class="theme-color">Sound Processor</h3>
                        <p>It helps in capturing sounds and converting them into digital signals. It further sends the
signals directly to the internal device.</p>

                        <h3 class="theme-color">Coil/Cable Unit</h3>
                        <p>It links the sound processor with the internal implant inside the skin. It helps in
transmitting electrical impulses to enable hearing.</p>

<h3 class="theme-color">Internal Device</h3>
<p>It is the main element of the cochlear implant, consisting of the electrode array. It is
implanted surgically into the cochlea of the ear. The electrodes further stimulate the
patient’s hearing nerve and enable the brain to perceive the signals on hearing sounds.</p>

<h2 class="theme-color">How Does a Cochlear Implant Device Work?</h2>
<p>In normal ears, the sound is usually transmitted via air, enabling the eardrum and the
middle ear bones (ossicles) to vibrate. Due to this, a vibratory wave is passed into the
cochlea (or the inner ear). These vibratory waves then convert into electrical signals that
are sent along the auditory nerve to the person’s brain.</p>
<p>However, a hearing-impaired person won’t have a functioning inner ear. With the help of
the cochlear implant, ENTs try to replace the function of the cochlea by converting
mechanical energy into electrical energy that’s further used for stimulating the cochlear
nerve to send the “sound” signals to the person’s brain.</p>
<p>But even with a cochlear implant, the patient still has hearing loss. As the cochlear
implant allows the patient only to hear the sounds – NOT understand them. It is only
useful in a quiet environment – NOT Noisy Situations!</p>

