                        <h2 class="theme-color">Eye Treatments Offered by SCI International hospital</h2>
                        
                        <h3>Cataract </h3>
                        <p>A cataract is a cloudy area in the lens of the eye that makes the vision blurry. Surgery is the only option to treat cataract. We practice Phacoemulsification which is a micro-incision technique with foldable Intraocular lenses (IOLs). The different types of IOLs include Aspheric, Toric and Multifocal. </p>
                        
                        <h3>Refractive Error </h3>
                        <p>Refractive errors are the most common eye problems which lead to vision related problems. It includes myopia, hyperopia, astigmatism and presbyopia. An individual needs glasses, contact Lenses, and in some cases LASIK Laser treatment (Customized, Intra lase), and use of Implantable Collamer lenses (ICL).  </p>
                        
                        <h3>Retinal Detachment, Vitreous Hemorrhage, and other retinal diseases </h3>
                        <p>These conditions happen due to bleeding or the contraction of the vitreous gel in the eye. To treat the conditions of retinal detachment, vitreous hemorrhage and othe retinal diseases we do both lasers and surgical treatment.</p>
                        
                        <h3>Age-related Macular Degeneration (ARMD)</h3>
                        <p>It is an eye disorder associated with aging and results in damaging sharp and central vision. It can blur your central vision and happens when the part of retina called the macula is damaged. It can be treated by FA, OCT, Laser and with the use of intravitreal Injections of Lucentis/Avastin/Macugen/ Steroids.  </p>
                        
                        <h3>Glaucoma </h3>
                        <p>It is a group of eye conditions that damage the optic nerve. There are different diagnostic procedures which include OCT, Visual field analyzer, pachymetry and are treated by Laser and Surgical Treatments. </p>
                        
                        <h3>Cornea Scar </h3>
                        <p>It is the irregularity in the cornea that limits the cornea ability to accurately focus light and most commonly caused by infections. We perform penetrating Keratoplasty that is a kind of Corneal Transplant.  </p>
                        
                        <h3>Keratoconus</h3>
                        <p>This is condition with which occurs on the cornea bulges outward into a cone shape. It is treated with Corneal Collagen Cross-linking with Riboflavin (C3R) and Intac’s Surgery. </p>
                        
                        <h3>Squint</h3>
                        <p>It is an eye condition in which the eyes point out in different directions. We do proper Check-ups and use of Glasses, Exercises and perform surgery to treat it properly. </p>
                        
                        <h3>Oculoplastic Cosmetic Eye Surgery</h3>
                        <p>It is a cosmetic eye surgery that corrects all the abnormalities related to eyelid and eye and tear duct. The excess and sagging skin from the lid are treated to give a better appearance.  </p>
                        
                        <h3>Trauma and Emergency Services </h3>
                        
                        <p>The sudden trauma and emergency eye services are treated on priority by our experienced team of eye doctors. Eye trauma need proper and complete examination as they are very serious and need immediate attention to prevent total loss of vision.  </p>
                        <p>Eye problems refer to a condition that affects the eyes and vision. Some eye problems are natural consequences of aging but others are from eye damage and other underlying conditions. Anyone facing eye problems should consult an eye specialist and also follow a healthy lifestyle with regular eye check-ups can prevent eye problems from occurring. </p>
                        
                      
