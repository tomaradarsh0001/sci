                        <h3 class="blog_title">Tips for Breast-feeding- Things that new moms should know</h3>
                        <p>The journey from pregnancy to motherhood can be a tough ride. Once the baby is born, the new parents are happy and nervous of the new journey ahead. As a new mom, it can get overwhelming to do everything for the first time.</p>
                        <p><b>Breastfeeding,</b> also referred to as <b>nursing</b>, is when breast milk is fed to the newborn baby. Breast milk provides the ideal amount of nutrients for a newborn. It has the perfect balance of vitamins, proteins and fat, required for a baby to be healthy. Breast milk contains antibodies that help the baby’s body to fight with various viruses and bacterias. It can be easily digested by a newborn baby, unlike formula.</p>
                        <p>There are many benefits of breastfeeding. Some them are-</p>
                        <ul class="list_blog">
                            <li>Easily digestible by baby.</li>
                            <li>Reduces risk of severe infections & diseases in babies.</li>
                            <li>Promotes healthy weight of baby.</li>
                            <li>Reduces disease risk in mothers.</li>
                            <li>Contains important antibodies for the baby.</li>
                        </ul>
                        
                       
                        <p>It can be a natural instinct for a mother to feed her newborn baby. But the instinct to breastfeed might not come off as easily. It can be scary at first and may take some time to get used to it. To help you with it, here are some breastfeeding tips for first time mothers.</p>
                        <h5>Breastfeeding Tips for new moms</h5>
                        <p><b>Latching On</b></p>
                        <p>Just like you, the baby is also learning something. Feeding on breast milk for the first time can be a bit of hassle for the newborn baby. It can take some time and practice for the baby to latch on to your breast, properly. Make sure to get your baby’s latch checked from the expert before leaving the hospital. Improper breastfeeding latch can result in your baby getting less milk and make your nipples sore.</p>
                        <p><b>Breastfeeding right after birth</b></p>
                        <p>It is generally advised to breastfeed your baby within an hour of the delivery. This has many benefits to it. It can help strengthen your baby’s stamina and stabilize their blood sugar level. On top of this, breastfeeding right after birth can help you and your baby adapt to the process of latching on, more easily.</p>
                        <p><b>Have someone to help you</b></p>
                        <p>Breastfeeding for the first time moms can take some time to get acquainted with. It’s better to have someone around to help you with the baby. It can get stressful to manage everything on your own. You can ask any of your relatives or hire a nanny to extend a helping hand.</p>
                        <p><strong>Let your baby set the routine</strong></p>
                        <p>As a new mom, you might find it difficult to make a routine for breastfeeding. The baby knows when and how to nurse. Don’t set intervals or force feed them. Watch out for signs of the baby being hungry. Sometimes, your baby can feed for a few minutes or stretch it to thirty minutes. Don’t panic. Babies know how much they want to feed.</p>
                        <p><strong>Be Comfortable</strong></p>
                        <p>It’s very important to keep comfort in mind while feeding. Not just the baby’s but yours too. Sit in a reclined position and place the baby in your arms. The baby can sense your discomfort or get disturbed from constant squirming. If you are not comfortable in nursing while sitting, then lie on your side with the baby facing towards you.</p>
                        <p><b>Have a healthy lifestyle</b></p>
                        <p>Healthy lifestyle choices are very important during breastfeeding. There are some healthy choices that you need to practise, like:</p>
                        <ul class="list_blog">
                            <li>Stay hydrated</li>
                            <li>Have a healthy diet</li>
                            <li>Avoid smoking or drinking</li>
                            <li>Take enough rest</li>
                            <li>Discuss about all your medications with doctor before taking them</li>
                        </ul>
                        
                        <p>Remember to take care of your body. Sometimes, your nipples might get sore or milk can dry up on your nipples. Don’t worry, try to pat dry them when you see leakage.</p>
                        <p><b>Relax</b></p>
                        <p>Breastfeeding will take some time to adapt, for both the mom and the newborn. It can be difficult to master the process. Stay in touch with your doctor and discuss with them if you struggle with anything. Everything takes time. Have some patience and don’t feel discouraged. It can get tiring to breastfeed in regular intervals. Make sure to relax.</p>
                        <p>Follow these tips about breastfeeding your newborn baby and consult the doctor if you have any queries.</p>
