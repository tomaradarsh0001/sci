                        <h3 class="blog_title">Gestational Diabetes: How to Control It with Diet, Exercise, and More?</h3>
                        <p>Gestational Diabetes during pregnancy is a condition during which the blood sugar level becomes very high. This kind of diabetes can be risky for the mother and the baby. The baby can gain extra weight from the extra glucose present in the body</p>
                        <p>And sometimes even have serious complications like stillbirth.  whereas, the mother might face difficulty with the delivery. </p>
                        <p>In most cases, gestational diabetes disappears, as soon as the child is born. It is very important to keep a check on Gestational Diabetes.</p>
                        <p>With some simple steps, a pregnant woman can easily stay healthy during pregnancy.</p>
                        
                        <h5>Diet Tips</h5>
                        <p>A pregnant woman can suffer from a condition known as gestational diabetes. Based on a study, this form of diabetes affects 10% of pregnant women. However, one gets to see two categories of gestational diabetes. While the class A1 diabetes could be managed through diet and exercise; whereas class A2 needs to be managed through insulin shots and medication. </p>
                        <p>Here are some tips that a pregnant woman should follow. It has been seen that controlling blood sugar during pregnancy is very crucial. When the blood sugar is kept under control, it won’t affect the health of the pregnant woman nor the baby. For controlling the blood sugar level, sweets and desserts must be avoided at all costs. Other factors to keep in mind are:</p>
                         <ul class="list_blog">
                            <li>Eating too much food at a time can lead to an increased blood sugar level. The nutritional need of any pregnant woman is high but eating small meals and snacks throughout pregnancy is highly recommended.</li>
                            <li>A reasonable amount of starch should be consumed. A proper serving size is 1cup of cooked grains, rice, potatoes or noodles, or 2 pieces of bread every meal.</li>
                            <li>Milk is an important source of calcium. However, consuming too much milk at a time can be a cause of high blood sugar. Limiting milk consumption to one cup is recommended.</li>
                            <li>Fruits have high nutritional value but the amount of natural sugars in it is very high. So fruits should be limited to one serving at a time.</li>
                            <li>Meals should focus on protein. While processed food must be limited, a lot of fresh foods should be included.</li>
                            <li>Pregnant women suffering from gestational diabetes should add whole grain bread or whole grains or millets in their diet. Sugary drinks and baked foods should be avoided at all costs.</li>
                        </ul>
                        
                        
                        <h5>Exercise Tips</h5>
                        <p>Exercising during pregnancy can prevent a pregnant woman from developing gestational diabetes. However, proper exercise can reduce the high glucose level in blood. </p>
                        <p>When a pregnant woman starts to exercise, the muscles start to consume more glucose. </p>
                        <p>As the effect of exercise starts to wane, muscles remain sensitive to insulin. This, in turn, reduces the blood glucose level. A moderate level of physical activity per day is ideal to deal with gestational diabetes. A 30-minute exercise could be a little problematic for those who are starting new. Hence, they can start working out for 15 minutes at a stretch and gradually increase their time.</p>
                        <p>Dealing with gestational diabetes without consulting a doctor could be risky. It’s better to follow the advice of the doctor to lead a healthy pregnancy.</p>
                       
                        
                      
                        
