                        <h3>Tips for Maintaining the Right Sitting Posture during pregnancy  </h3>
                        <p>If you are a working professional, maintaining proper posture during pregnancy while sitting is necessary. Let’s take a look at the tips for maintaining the correct postures during pregnancy.  </p>
                        <ul class="list_blog">
                            <li>Make sure you don’t sit for long hours at a stretch during the 1st trimester of pregnancy. Take short breaks after every one hour and whenever you are sitting, keep your back and shoulder straight. </li>
                            
                        </ul>
                        <p>As the baby starts to grow from the 2nd trimester onwards, you will need to change the way you used to sit.  </p>
                        <ul class="list_blog">
                            <li>While sitting, don’t forget to keep your back straight and the shoulders must be pulled back. The buttocks should reach the back of the chair. </li>
                            <li>Choose a chair that comes with good back support. Place a pregnancy pillow behind the lower-back portion. </li>
                            <li>Use a footstool and keep your feet raised. It would reduce the strain on the lower back. If you don’t have a footstool, your feet should be placed evenly on the floor. The knees and the hips must be at 90 degrees. </li>
                        </ul>
                       
                        <p>In the event, you are not using a chair with lumbar support; you should keep in mind the following things. They are:  </p>
                        <ul class="list_blog">
                            <li>Don’t cross your legs while sitting. It can affect blood circulation. </li>
                            <li>You should slightly tilt your pelvic forward so that sway back position is avoided.  </li>
                            <li>Don’t slouch while sitting on the chair. </li>
                            <li>If you are sitting on a chair that rolls, don’t just turn your waist. Instead, you should turn your whole body. </li>
                            <li>Adjusting the chair height with the workstation is vital to keep the shoulder relaxed. Make it a point to sit up close to your desk. Allow the elbows and arms to rest on the chair/ desk. </li>
                        </ul>
                        
                        
                        
