                                            <h2 class="theme-color">Types of Ovarian Cysts </h2>
                                            
                                            <h3 class="theme-color">Functional Cysts: </h3>
                                            <p>The most common type of ovarian cysts, is not associated with diseases, but rather a part of the menstrual cycle.</p>
                                            <p><b>Formation:</b> Result from ovulation, the release of an egg from the ovary.</p>
                                            <h3 class="theme-color">Subtypes:</h3>
                                            
                                            <p><b>Follicular Cysts:</b> Occur when a sac (follicle) in the ovary fails to release an egg, filling with fluid and enlarging.</p>
                                            <p><b>Corpus Luteum Cysts:</b> Form after the release of an egg when fluid accumulates in the corpus luteum, a hormone-producing cell group.</p>
                                            
                                            <h3 class="theme-color">Other Cysts:</h3>
                                            <p><b>Overview: </b>Ovarian cysts that don't directly correlate with the menstrual cycle, not necessarily indicating disease but requiring monitoring.</p>
                                            <p><b>Subtypes:</b></p>
                                            <p><b>Cystadenomas:</b> Form on the ovary's surface, containing either thin, watery fluid or thicker, mucus-like material.</p>
                                            <p><b>Dermoid Cysts (Teratomas):</b> Composed of cells representing various human tissues, including skin, hair, teeth, and even brain tissue.</p>
                                            <p><b>Endometriomas:</b> Filled with endometrial tissue, the same tissue shed during menstrual periods.</p>
                                            <p><b>Ovarian Cancer:</b> Solid masses of cancer cells, distinct from benign cysts, indicating a more serious condition.</p>
                                            
