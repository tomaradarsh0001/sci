                                            <h2 class="theme-color">How are uterine fibroids treated?</h2>
                                            <h3 class="theme-color">Observation and Follow-Up</h3>
                                            <p>If fibroids are small (less than 3 cm) and asymptomatic, a watchful waiting approach is adopted, with regular ultrasound follow-ups to monitor their size.</p>
                                            
                                            <h3 class="theme-color">MR-guided Focused Ultrasound Surgery (MRgFUS)</h3>
                                            <p>Non-invasive MRgFUS employs ultrasonic pulses to heat and eliminate fibroids, guided by an interventional radiologist; caution is advised for very large or multiple fibroids, with limited data on post-procedure childbearing safety.</p>
                                            
                                            <h3 class="theme-color">Uterine Artery Embolization (UAE) </h3>
                                            <p>UAE involves injecting embolic agents into arteries to cut off blood flow, causing fibroid shrinkage; not ideal for future childbearing or large fibroids.</p>
                                            
                                            <h3 class="theme-color">Myomectomy (Laparoscopic/Robotic/Open) </h3>
                                            <p>Myomectomy selectively removes fibroids, preserving the uterus; laparoscopic or robotic methods offer less pain, smaller incisions, and faster recovery, while open myomectomy is preferred for larger or complex fibroids.</p>
                                            
                                            <h3 class="theme-color">Hysteroscopic Surgery</h3>
                                            <p>Hysteroscopic approach addresses submucous and pedunculated fibroids via the vagina, sometimes with preoperative measures for very large fibroids, aiding in hemoglobin build-up.</p>
                                           
