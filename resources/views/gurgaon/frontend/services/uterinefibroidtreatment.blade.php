                                            <p><b>Intramural Fibroids:</b> Embedded within the muscular wall of the uterus, these fibroids expand, potentially causing the uterus to stretch.</p>
                                            <p><b>Subserosal Fibroids:</b>  Growing on the outer surface of the uterus, they can reach a size that distorts the uterine shape, appearing prominently on one side.</p>
                                            <p><b>Submucosal Fibroids:</b>  Developing in the middle muscle layer (myometrium) of the uterus, these fibroids can affect the uterine cavity.</p>
                                            <p><b>Pedunculated Fibroids: </b> Stemming from subserosal fibroids, these have a slender base (stem) supporting the tumor, giving them the name pedunculated fibroids.</p>
