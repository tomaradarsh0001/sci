                        <h3 class="theme-color">Advantages of Minimal Access (Laparoscopic) Surgery</h3>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i>  Smaller Incisions: Unlike traditional surgery that requires large incisions, the new
minimally invasive surgery depends only on small incisions for inserting cameras,
thin tubes or other surgical tools for treatment.</li>
                            <li><i class="fa fa-check"></i>  Less Pain: This procedure is less painful and discomforting during and after the
treatment.</li>
                            <li><i class="fa fa-check"></i> Shorter Hospital Stay: The patients can go home sooner and also return to work
quickly after this procedure.</li>
                            <li><i class="fa fa-check"></i>  Faster Recovery: Since the size of the incision is small, you’ll heal faster than the
open surgery.</li>
                            <li><i class="fa fa-check"></i>  Less Scarring: Minimally invasive surgery needs small incisions. This means
there will be very few noticeable scars.</li>
                        </ul>
                        <p>Increased Accuracy: Compared to traditional open surgery, minimally invasive surgery
offers higher accuracy and it utilizes advanced equipment and tools.</p>
                        
                        
