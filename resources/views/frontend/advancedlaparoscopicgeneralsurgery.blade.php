                        <h2 class="theme-color">Advanced Laparoscopic Surgery</h2>

                        <p>Advanced laparoscopic surgery is a minimally invasive surgery. In this procedure, the
surgeon usually operates through tiny incisions made in the skin instead of the large
incision for performing an open operation.</p>
                        <p>What makes this type of surgery more advanced is the type of procedures that general
surgeons perform with this technique. For instance, foregut surgery, liver resections,
colon surgery, total gastrectomies and other major operations are done laparoscopically
by general surgeons.</p>
                        <p>The advantages of advanced laparoscopic surgery are that it’s less painful, minimal to
no scarring and quicker recovery. However, the best surgical method is figured out
based on a specific patient’s condition. So, make sure to consult your surgeon about the
treatment that is right for you.</p>
                        
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Surgery Types In Laparoscopic</h2>

                        <p>With technical advances nowadays, surgery doesn’t need large incisions as it was done
in the past. There are many surgical procedures like:</p>
                        <p>Open surgery: It means that the surgeon will cut the skin and tissues to fully view and
access the organs and structures involved. For example, open surgery requires the
removal of organs like kidneys or gall bladder.</p>
                        <p>Minimally invasive surgery: This advanced type of surgical technique doesn’t require a
large incision. It is a relatively new technique of recuperating a patient with less pain.
However, all conditions are not suitable for treatment with minimally invasive surgery.
The common surgery methods that fall under minimally invasive surgery are:</p>
                        
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i>  Endoscopy</li>
                            <li><i class="fa fa-check"></i>  Laparoscopy</li>
                            <li><i class="fa fa-check"></i>  Cystoscopy</li>
                            <li><i class="fa fa-check"></i>  Bronchoscopy</li>
                            <li><i class="fa fa-check"></i>  Arthroscopy</li>
                            <li><i class="fa fa-check"></i>  Gastroscopy</li>
                            <li><i class="fa fa-check"></i>  Sigmoidoscopy</li>
                            <li><i class="fa fa-check"></i>  Laryngoscopy</li>
                            <li><i class="fa fa-check"></i>  Hysteroscopy</li>
                        </ul>
                        <p>Besides using conventional surgical knives for surgery, the open and minimally invasive
surgeries can also involve alternative techniques (depending on the procedure), such
as:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i>  Electrosurgery</li>
                            <li><i class="fa fa-check"></i>  Laser surgery</li>
                        </ul>
                        
                        
