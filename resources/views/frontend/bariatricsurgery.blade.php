                    <div class="pt-2 pt-md-4">
                        <p>Bariatric surgery is an operative procedure that helps patients’ to lose weight making certain changes to the digestive system. Some bariatric surgery types also make the stomach smaller, enabling the person to eat and drink less and making them feel full soon. Other bariatric surgery types can also make changes to the small intestine that absorbs nutrients and calories from food items.</p>
                        <p>Bariatric surgery is an option for people with severe obesity and those who are not able to lose weight or keep gaining weight even after making healthy lifestyle changes. Bariatric surgery is also helpful if anyone has serious health issues like sleep apnoea, type 2 diabetes or other conditions linked to obesity.</p>
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">What is the Need Of Bariatric Surgery?</h2>

                        <p>Bariatric surgery is done to let an individual lose excess weight and decrease the risk of some weight-related health conditions, such as:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Heart disease</li>
                            <li><i class="fa fa-check"></i> High blood pressure</li>
                            <li><i class="fa fa-check"></i> Type 2 diabetes</li>
                            <li><i class="fa fa-check"></i> Stroke</li>
                            <li><i class="fa fa-check"></i> Severe sleep apnoea</li>
                        </ul>
                        <p>This surgery is done only when you have tried different things to lose weight and nothing has worked accordingly.</p>
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Why Someone Need Bariatric Surgery ?</h2>

                        <p>Generally, bariatric surgery is an option for obese people if:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Their body mass index is around 40 or higher (extremely obese).</li>
                        </ul>
                        <p>Their BMI is around 35 to 39.9 (obese) and also have any serious weight-related health conditions like high blood pressure, type 2 diabetes or severe sleep apnoea. In some cases, bariatric surgery is also recommended if a person’s BMI is 30 to 34 with a serious weight-related medical condition.</p>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h2 class="theme-color">Types of Bariatric Surgery</h2>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Roux-en-Y (Gastric Bypass Surgery): This surgery is done to reduce the amount of food a person can eat in one go and reduce the absorption of nutrients.</li>
                            <li><i class="fa fa-check"></i> Biliopancreatic diversion with duodenal switch: This surgery bypasses most of the intestine by linking the end portion of the small intestine to duodenum around the stomach and hence, called the duodenal switch and biliopancreatic diversion.</li>
                            <li><i class="fa fa-check"></i> Laparoscopic adjustable gastric banding (LAGB): LAGB re-positions the inflatable stomach band around the topmost part of the stomach. When inflated, this band compresses and tightens the stomach like a belt. This band can also be adjusted to limited food intake.</li>
                            <li><i class="fa fa-check"></i> Sleeve gastrectomy: Also known as vertical sleeve gastrectomy, this latest weight-loss surgery changes the structure of the stomach in the form of a tube to restrict the calories a body can absorb.</li>
                        </ul>
                        
                        
                        <div class="mt-3 mt-lg-6"></div>

                        <h3 class="theme-color">Benefits of Weight loss surgery</h3>
                        
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Bariatric surgery reduces the risk of health problems by losing weight via surgery compared to remaining obese.</li>
                            <li><i class="fa fa-check"></i> Weight loss surgery not only makes you look great and feel better but also ensures you lead a healthy life.</li>
                            <li><i class="fa fa-check"></i> Weight loss improves the lifestyle and physical conditions related to being overweight.</li>
                            <li><i class="fa fa-check"></i> If you have tried many exercises or diet plans to lose weight but couldn’t get the desired results, bariatric surgery is the most effective solution offering significant and long-term weight loss.</li>
                            <li><i class="fa fa-check"></i> With reduced weight, the energy level of people increases automatically, enabling them to move around conveniently with improved quality of life.</li>
                            <li><i class="fa fa-check"></i> Successful bariatric surgery also improves emotional health and prevents depression in people suffering from social stigma and poor body image issues.</li>
                            <li><i class="fa fa-check"></i> Achieving and maintaining a normal weight enables people with sleep apnoea to avoid their CPAP machine during bedtime.</li>
                        </ul>
                        
                        <div class="mt-3 mt-lg-6"></div>
                        <h3 class="theme-color">What are the Risks Factors of weight loss surgery?</h3>
                        <p>Just like any major surgery, bariatric surgery and other weight-loss surgical procedures also pose some potential short-term, as well as the long-term, health risks.</p>
                        <p>Short-term risks associated with the bariatric surgical procedure are as follows:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Excessive bleeding</li>
                            <li><i class="fa fa-check"></i> Blood clots</li>
                            <li><i class="fa fa-check"></i> Infection</li>
                            <li><i class="fa fa-check"></i> Acid reflux</li>
                            <li><i class="fa fa-check"></i> Dilation of oesophagus</li>
                            <li><i class="fa fa-check"></i> Chronic vomiting or nausea</li>
                            <li><i class="fa fa-check"></i> Anaesthesia-related risks</li>
                            <li><i class="fa fa-check"></i> Inability to have certain foods</li>
                            <li><i class="fa fa-check"></i> Obstruction of stomach</li>
                            <li><i class="fa fa-check"></i> Breathing or lung problems</li>
                            <li><i class="fa fa-check"></i> Weight gain or inability to lose weight</li>
                            <li><i class="fa fa-check"></i> Adverse reactions to anaesthesia</li>
                            <li><i class="fa fa-check"></i> Leaks in the gastrointestinal system</li>
                            <li><i class="fa fa-check"></i> Death (rare)</li>
                        </ul>
                        <p>On the other hand, long-term complications and risks of undergoing the weight-loss surgery may vary as per the type of surgery. Some of these risk factors include:</p>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Hernias</li>
                            <li><i class="fa fa-check"></i> Ulcers</li>
                            <li><i class="fa fa-check"></i> Gallstones</li>
                            <li><i class="fa fa-check"></i> Bowel obstruction</li>
                            <li><i class="fa fa-check"></i> Malnutrition</li>
                            <li><i class="fa fa-check"></i> Lowe blood sugar (hypoglycaemia)</li>
                            <li><i class="fa fa-check"></i> Vomiting</li>
                            <li><i class="fa fa-check"></i> Stomach perforation</li>
                            <li><i class="fa fa-check"></i> Dumping syndrome, leading to nausea, diarrhoea or vomiting</li>
                            <li><i class="fa fa-check"></i> Death (rare)</li>
                        </ul>
