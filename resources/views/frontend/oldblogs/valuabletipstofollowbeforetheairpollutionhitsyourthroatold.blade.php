                        <h4 class="blog_title">10 Valuable Tips to Follow Before the Air Pollution Hits Your Throat</h4>
                        <p>As the winds have started to become chilly, the worry about air pollution has also started to come up in the minds of people. The polluted air not only makes people cough but it also causes throat irritation and enhances the chances of viral infections.</p>
                        <p>The people who suffer from respiratory troubles, like asthma, are the worst victim of air pollution. However, there are some preventive measures that you can follow to make sure that air pollution does not harm you as much. In the below section of this article, we have gathered 10 tips that can protect you from air pollution in the upcoming months.</p>
                        <h5>1. Refrain From Morning Walks</h5>
                        <p>Regular exercise is the only way to stay fit for the rest of your life. But you should avoid taking walks or refrain from morning jogging when the quality of the air becomes subpar during the winters. Instead, shift these physical activities to evening.</p>
                        
                        <h5>2. Use Face Masks</h5>
                        <p>In the last few months, the N-95 masks have become a regular part of our life. Well, these are the masks that can protect you from pollution as well. Whenever you step out of your home, make sure to wear the masks.</p>
                        
                        <h5>3. Air Purifying Plants</h5>
                        <p>Plant the air-purifying trees, like aloe vera, Ivy, Neem and spider plants and place them in your home and office. These plants will keep the indoor air clean and will minimise the indoor pollution.</p>
                        
                        <h5>4. Ventilate Your Bathroom and Kitchen</h5>
                        <p>To retain the indoor air pollution to a minimum, you should make sure that there is a chimney in your kitchen and an exhaust in the bathroom. These will recirculate the air and bring fresh air inside your home.</p>
                        
                        <h5>5. Restrict Outdoor Activities for Kids</h5>
                        <p>You should not allow kids, below 8 years old, to play outdoors during this time. If possible, you should also request the school authorities for suspending all the outdoor activities as well.</p>
                        
                        <h5>6. Use Air Purifiers</h5>
                        <p>Use of air purifiers in your home is a must, especially in the rooms where the kids stay. The rooms of the elderly members also require air purification. The kids and the elderly members of the households are the most prone to the ill effects of the toxic air.</p>
                         
                        <h5>7. Take Steam</h5>
                        <p>Put a few drops of eucalyptus oil into boiling water and inhale the steam. It will relax your air passages and help your body to remove the harmful particulate substances.</p>
                        
                        <h5>8. Purify the Car Air</h5>
                        <p>While starting your car every morning, roll out the windows to let the air circulate. Also, the car AC should be turned on in the indoor circulation mode that reduces the PM 2.5 level considerably.</p>
                        
                        <h5>9. Ventilate Your Home</h5>
                        <p>You should open the doors and windows between 3 pm and 5 pm and allow the air to circulate. During any bright sunny day, this is the time when the concentration of PM 2.5 stays lowest in the air.</p>
                        
                        <h5>10. Herbal Tea</h5>
                        <p>Consume a couple of cups of herbal tea to flush out the pollutants from your lungs.</p>
