                        <h3>Common Gallbladder Problems  </h3>
                        
                        <h3>Gallstones  </h3>
                        <p>They are also known as gallbladder stones; it is one of the most commonly heard problems that arise from an unhealthy gallbladder. A sudden painful cramp in your upper abdomen may be a symptom of this. It’s important to treat these immediately before it turns perilous.  </p>
                        <p>Sometimes, these stones may get stuck in your bile duct and block the secretion of bile. That’s when you need immediate medical care.  </p>
                        
                        <h3>Cholecystitis  </h3>
                        <p>Inflammation in the gallbladder is known as cholecystitis. This can be the result of Gallstones, bacteria, fungi, or tumors. The gallbladder gets swollen and you can feel a shooting pain in your belly. Immediate treatment needs to be followed and will include the removal of the gallbladder.  </p>
                        
                        <h3>Perforated Gallbladder  </h3>
                        <p>Formation of tiny holes in the gallbladder results in a perforated bladder. When the gallstones are not treated on time, they create holes in the walls of your gallbladder. This can turn into a life-threatening situation. Consult your doctor immediately if you feel nauseous or have a high fever.  </p>
                        
                        <h3>Gallbladder Cancer</h3>
                        <p>It is very rare and difficult to detect. Gallbladder cancer shows no symptoms at the early stages. Severe abdominal pain, sudden weight loss, jaundice, or bloating are some symptoms of gallbladder cancer. An unhealthy lifestyle is the main reason for this. Gallstone also must be associated with cancer in later life  </p>
                        
                        <h3>Empyema</h3>
                        <p>It is also known as a gallbladder abscess. This arises due to an infection from the buildup of bile. It requires professional care. The infection will spread faster & needs earlier removal of the gallbladder.  </p>
