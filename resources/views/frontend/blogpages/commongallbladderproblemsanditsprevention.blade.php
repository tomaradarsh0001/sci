                        <ul class="list_blog">
                            <li>Fever & Nausea</li>
                            <li>Fatigue</li>
                            <li>Itchy sensation</li>
                            <li>Sudden painful cramps</li>
                            <li>Chills & vomiting</li>
                            <li>Jaundice</li>
