                        <h2>What is Gallbladder disorder?  </h2>
                        <p>The gall bladder is an organ that stores bile and passes it on to the intestine during the digestion process. It is positioned right under our liver. Do you feel sudden pain at times from the stomach area? We don’t give much thought to that. But this could mean that there is some problem with the bile duct which can hinder our digestion.  </p>
