                        <h1 class="blog_title">Overview on Common Dental Infections and their Prevention </h1>
                        <p>Oral care is as important as taking care of any other body part and should be an important part of our daily routine. But it is mostly neglected by most people on a daily basis. Oral care refers to taking care of your mouth, tongue, and teeth. It does not just end with brushing your teeth as a morning ritual, there’s more to it. People don’t take dental health care seriously, which later results in severe problems. Oral cancer also results from non-maintenance of dental hygiene and is life-threatening, if not treated at the earliest. </p>
                        <p>You will be surprised to know that India has the highest number of oral cancers and other dental problems. The majority of people do not know about basic dental hygiene. They are unaware of the problems caused by poor oral health.  </p>
                        
                        <h2>Common Dental infections </h2>
                        <p>Let’s know about some common dental infections. </p>
                        
                        <h3>Bad Breath  </h3>
                        <p>Bad breath, also referred to as halitosis, is a very common oral problem. This results from poor dental hygiene and an unhealthy lifestyle. Different kinds of food like garlic, spices, soda, tobacco, and alcohol, can also contribute to bad breath.  </p>
                        <p>People with improper diets who eat less are also at the receiving end of bad breath. This is because when the body breaks down fat through chemical reactions, it can give an unpleasant smell to your breath. Diabetes or any gastric disease can also cause bad breath. This can be prevented with proper dental hygiene.  </p>
                        <p>For persistent issues, consult your dentist promptly. Bad breath may signal other dental problems, like dry mouth or periodontal disease. Mouthwash masks odor but doesn't treat underlying issues.  </p>
                        
                        
                        <h3>Cavities/ Tooth Decay </h3>
                        <p>Cavity refers to the formation of tiny openings or holes in the teeth due to tooth decay. Tooth decay arises when mouth acids harm the outer enamel and inner dentin layers. This common health issue, known as cavities, results from the deterioration of dental structures. </p>
                        <p>While it’s a common misconception that cavities can only occur in kids or teenagers, it also affects adults majorly. Foods with high carbs can be a major cause of carbohydrates. Neglecting dental care in childhood weakens teeth, potentially causing eventual tooth decay.  </p>
                        <p>Some symptoms of cavities include-  </p>
                        <ul class="list_blog">
                            <li>Spontaneous Toothaches</li>
                            <li>Sensitive teeth</li>
                            <li>Pain when you bite down</li>
                            <li>White, black or brown stains</li>
                            <li>Pits or holes in teeth</li>
                        </ul>
                        
                       
                        <p>Cavities, if untreated, can cause major dental problems and affect deeper layers of the teeth. If you feel your teeth need special care, SCI offers the <a href="https://scihospital.com/gurgaon/dental-implants">best dental implants in Gurgaon</a>.  </p>
                        
                        
                        <h3>Tooth Abscess </h3>
                        <p>A tooth abscess is commonly known as a root infection. When a pocket of pus or abscess forms on the root of the teeth due to infection; it causes tooth abscess. Improper dental hygiene and a sugary diet are the common causes of root infections. Resulting from issues like decay, gum diseases, or a cracked tooth, it's a common dental concern.  </p>
                        <p>Signs and symptoms of a tooth abscess include-  </p>
                        <ul class="list_blog">
                            <li>Swelling in cheek or face</li>
                            <li>Tooth sensitivity</li>
                            <li>Foul smell when chewing with the infected tooth</li>
                            <li>Pain while chewing</li>
                        </ul>
                        <p>Root canal treatments are done to treat root infections.  </p>
                        
                        
                        <h3>Dry mouth</h3>
                        <p>A dry mouth is a condition, wherein the salivary glands don’t produce enough saliva to keep the mouth moist. This is also known as Xerostomia. Saliva is very important to help us digest our food properly and cleanse our mouths. This is also known as Xerostomia. </p>
                        <p>It can be caused due to the following reasons- </p>
                        <ul class="list_blog">
                            <li>Stress & anxiety</li>
                            <li>Intake of tobacco or drugs</li>
                            <li>Side effects of certain medications</li>
                            <li>Dehydration</li>
                            <li>Nerve damage</li>
                        </ul>
                        <p>Dry mouth symptoms, like throat dryness, sticky mouth, speech or eating difficulties, bad breath, and taste changes, require varied treatments based on their cause. Neglected, it may escalate to issues such as gingivitis or dental problems. Preventive measures  </p>
                       
                        <h3>Gum Disease</h3>
                        <p>Gum diseases like periodontitis & gingivitis are very common. Neglected periodontitis can result in severe tooth loss without proper treatment. Prioritize timely and effective care. It starts with bacterial growth in the mouth. Gingivitis is inflammation of the gum caused by bacterial infection  </p>                        
                        <p>Some causes of gum disease can be- </p>  
                        <ul class="list_blog">
                            <li>Hormonal changes</li>
                            <li>Diseases such as cancer or HIV</li>
                            <li>Smoking</li>
                            <li>Certain medications</li>
                            <li>Poor oral hygiene</li>
                        </ul>
                        
                        
                        <p>The symptoms of gum diseases might not be noticeable till later stages. Some signs can include- gum bleeding during brushing, bad breath or bad taste, receding gums, red swollen gums, or formation of pockets between teeth & gums.  </p>                        
                        <p>Gum diseases can be prevented and prevented from getting worse. Maintaining proper dental hygiene is very important to avoid these diseases.  </p>                        
                        
