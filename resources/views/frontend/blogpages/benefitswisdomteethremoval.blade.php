                      <h3>What happens during the process of extraction?</h3>
                      
                      <p>The process of wisdom teeth extraction follows these methods:</p>
                      <p><b>Anesthesia:</b> Local anesthesia numbs the area around the wisdom tooth to minimize pain during the procedure.</p>
                      <p><b>Incision:</b> Sometimes, an incision in the gum tissue is necessary to access the tooth.</p>
                      <p><b>Tooth Removal:</b> The dentist uses specialized instruments to carefully extract the wisdom tooth from its socket.</p>
                      <p><b>Stitching:</b> If needed, stitches are used to close the incision and aid in healing.</p>
                      <p><b>Gauze Placement:</b> Gauze is positioned at the extraction site to control bleeding and promote clot formation.</p>
                      
                      
                      <h4>Conclusion</h4>
                      <p>Wisdom tooth removal is an important aspect because wisdom tooth not only brings maturity but also a lot of pain and problems. If it causes you any trouble, it should be removed as soon as possible to improve general oral health, avoid complications, infections, and long-term effects, and ensure a brighter, pain-free dental future.</p>
                      
