                        <h3 class="blog_title">Check Out Some Important Prenatal Nutrition Tips for World Nutrition Day</h3>
                        <p>Pregnancy is considered to be a beautiful moment for would-be-mothers. However, during pregnancy, mothers-to-be go through many ups and downs. To ensure a healthy pregnancy, a proper diet, and a healthy lifestyle need to be maintained.  </p>
                        <p>According to some experts, healthy nutrition at the time of pregnancy not only helps with fetal development but also reduces the chance of the individual and the baby getting exposed to chronic disease. So during pregnancy, would-be-mothers should avoid a strict diet plan. Rather, one should eat wholesome nourishing foods as its good for the health of the baby and the mother.</p>
                        <h5>Key Nutrition</h5>
                        <p>Whatever a woman eats and drinks during pregnancy, it becomes the main source of nourishment for the baby. Hence, a would-be mother’s diet should contain all important nutrients that would help in the growth and development of the baby. </p>
                        <p>A pregnant woman needs a proper amount of calcium, iron, folic acid, and protein. For instance, a bowl of salad containing wholesome fruits and vegetables would be great during pregnancy.</p>
                        
                        <h5>Prenatal Nutrition Tips</h5>
                        <p>Every nutrient is important for the baby and the mother during the stages of pregnancy.</p>
                        
                        
                        <p><b>Folic Acid:</b> Pregnant women should consume food items like almonds, legumes, dark green leafy veggies, avocado, etc as they are a good source of folic acid. </p>
                        <p><b>Protein:</b> Lean meat or fish can be a good source of protein during lunch. However, you can also go for a cheese slice or a yogurt tub if you like. </p>
                        <p><b>Iron: </b> Iron is an important nutrient during pregnancy. To get iron from food, you can try some red meat, as it is a good source of iron. Vegans can go for kidney beans, wheat germ, lentils, spinach, and even dry apricots for the same. </p>
                        <p><b>Zinc:  </b> It helps with the healthy development of the bone and the brain. Good sources of zinc are dairy products, fish, and meat. Similarly, nuts and legumes also offer zinc.</p>
                        <p><b>Calcium:  </b> Nutrients like calcium help with strengthening of the bones. Adding a small amount of Tofu or Brazilian nuts would be a good option for pregnant ladies.  </p>
                        
                        
                        <p>A good source to get Magnesium and Omega-3 is by consuming food like almonds, walnuts, tofu, salmon, sardine, and trout. </p>
                        
                        
                        <p>For omega-3 one shouldn’t take more than 2 portions every week. Hence, a pregnant woman should consume a wide range of healthy food, such as fruits, whole grain products, vegetables, and dairy products.</p>
                        
                        <h5>Food & Nutrition : </h5>
                        <p>The food pyramid will have starches i.e cereals, protein, fat, vegetables and fruits.</p>
                        <p>Proper, balanced food intake during pregnancy is essential and ensures sound health for the mother as well for the growing fetus. The recommended DAILY intake is:</p>
                         
                        <p>Cereals-(2 bread slices, 4 chapatis, 2 spoon rice, oats, or dal can be for breakfast).</p>
                        <p>Pulses-30 gms (10 gms sprouted, 20 gms boiled dal-three helpings)</p>
                        <p>Toned Milk-1000 gms for vegetarians (curds, home-made paneer, milk-in any from).</p>
                        <p>Vegetables-300-500 gms (fresh leafy and green vegetable ).</p>
                        <p>Roots and Tubers-50 gms (potato, radish, carrot, etc).</p>
                        <p>Fruit-4 (citrus fruits, 2 apples, 1 banana, 100 gms papaya, 1 mango)</p>
                        <p>Total Fat-20 gms (oil/ghee, which is used for cooking )</p>
                        <p>Sugar-10 gms</p>
                        <p>Nuts-8-10</p>
                        <p>Non-Veg-2 eggs per week, or 150 gms meat (preferably while ) fish, red meat, chicken, liver)</p>
