                        <p>Before the surgery, your doctor will provide you with detailed instructions on how to prepare your kid for the procedure. This may include instructions on how to feed your kid in the hours leading up to the surgery, what medications to avoid, and what to bring with you to the hospital. It's important to follow these instructions carefully to ensure the best possible outcome for your kid. </p>
                        
                        <h3>During Hypospadias Surgery:</h3>
                        <p>During the surgery, your child will be placed under general anesthesia, which means they will be unconscious and feel no pain during the procedure. The surgery typically takes about 1-2 hours and involves making an incision in the underside of the penis and repositioning the urethral opening to the tip of the penis. Your child will be closely monitored by the medical team throughout the surgery to ensure their safety and comfort. </p>
                        
                        <h3>After Hypospadias Surgery:</h3>
