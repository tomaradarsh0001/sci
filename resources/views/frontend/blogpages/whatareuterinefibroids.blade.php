                  <h3>Heavy Menstrual Bleeding: </h3>
                  <p>Fibroids can cause excessive menstrual bleeding, leading to prolonged periods or the need for frequent changes in sanitary products. Women with fibroids may also pass blood clots during their menstrual cycle.
</p>
<h3>Pelvic Pain and Pressure:</h3>
<p>Fibroids can cause persistent pelvic pain or discomfort, which may be accompanied by a feeling of pressure in the lower abdomen. Some women may experience pain during sexual intercourse.
</p>
<h3>Urinary Problems:</h3>
<p>Large fibroids can put pressure on the bladder, leading to frequent urination, urgency, or difficulty emptying the bladder completely. In some cases, fibroids can even cause urinary tract infections.
</p>
<h3>
    Infertility and Pregnancy Complications:
</h3>
<p>Depending on their size and location, fibroids can interfere with fertility by blocking the fallopian tubes or disrupting the implantation of a fertilized egg. Fibroids may also increase the risk of complications during pregnancy, such as placental abruption, preterm labor, or a breech presentation.
</p>
