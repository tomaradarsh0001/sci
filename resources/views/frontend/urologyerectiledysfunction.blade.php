                            <li><i class="fa fa-check"></i> Vacuum Erection Device: It is a plastic tube that is fixed over the penis to seal it with the skin. A pump, at another end of this tube, makes a low-pressure vacuum around the erectile tissues, resulting in the erection. </li>
