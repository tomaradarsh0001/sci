                        <h4 class="theme-color">Diagnosis of Erectile Dysfunction </h4>
                        <ul class="services_list">
                            <li><i class="fa fa-check"></i> Physical examination: The first step of erectile dysfunction treatment is diagnosis. Initially, it includes careful examination of the penis and checking nerve sensation. </li>
                            <li><i class="fa fa-check"></i> Blood tests: The second step of erectile dysfunction treatment requires blood tests. A sample of blood is sent to a laboratory for checking signs of diabetes, heart disease, low testosterone levels, and other health problems. </li>
                            <li><i class="fa fa-check"></i> Ultrasound: The next step for erectile dysfunction treatment includes an ultrasound. It involves a transducer (wand-like device) to be held over blood vessels of the penis. It forms a video image to let the specialist see what blood flow problems exist. Sometimes, this test is also done in combination with injections of drugs into the penis for stimulating blood flow to get an erection. </li>
                            <li><i class="fa fa-check"></i> Urine tests (urinalysis): The final step before erectile dysfunction treatment can be started is a urinalysis.  Urine tests are also used for testing signs of underlying health conditions. </li>
                            <li><i class="fa fa-check"></i> Psychological exam: The specialist asks questions to determine depression or psychological causes leading to erectile dysfunction and prescribes erectile dysfunction treatment accordingly.  </li>
